echo "=== Creating git tags ==="
for entry in "${TAGS[@]}"; do
  read -r ver sha ts title <<< "$entry"
  tag="$ver"
  # Skip if tag already exists
  if git rev-parse "$tag" >/dev/null 2>&1; then
    echo "  skip $tag (already exists)"
    continue
  fi
  echo "  tag $tag @ ${sha:0:9} $ts"
  GIT_COMMITTER_DATE="$ts" git tag -a "$tag" "$sha" -m "$tag: $title"
done

echo ""
echo "=== Pushing tags ==="
git push origin --tags

echo ""
echo "=== Creating GitHub releases ==="
for entry in "${TAGS[@]}"; do
  read -r ver sha ts title <<< "$entry"
  tag="$ver"
  # Check if release already exists
  if gh release view "$tag" >/dev/null 2>&1; then
    echo "  skip $tag (release already exists)"
    continue
  fi
  echo "  release $tag: $title"
  gh api repos/tamnd/goipy/releases \
    -X POST \
    -f tag_name="$tag" \
    -f name="$tag" \
    -f body="$title" \
    -F draft=false \
    -F prerelease=false \
    -f published_at="$ts" \
    --silent
done

echo ""
echo "Done. $(echo "${#TAGS[@]}") releases backfilled."
